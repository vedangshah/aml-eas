<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;

class Userexam extends Model
{
    protected $table = 'user_exam';
    
    
    //many users can be associated with one exam
    public function user1() {
        return $this->belongsTo('App\Models\User','user_id');
    }
    
    public function exam2() {
        return $this->belongsTo('App\Models\Exam', 'exam_id');
    }
    //one user exam can have one exam result
    public function uer1() {
        return $this->hasOne('App\Models\Examresult',"user_exam_id");
    }






















































































































































































































    

       // one user exam can have many exam questions

    public function ueq() {
        return $this->hasMany('App\Models\UserExamQuestion',"user_exam_id");
    }
   
    
}
