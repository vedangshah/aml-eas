        $messages = [
            'user_email.required' => 'Please enter name of the email',
        ];

        $validator = Validator::make($request->all(), [
            'user_email' => 'bail|required',
        ], $messages);

        if($validator->fails()) {
            return redirect()->route('forgot-user.create')
                            ->withErrors($validator)
                            ->withInput();
        }
    }

